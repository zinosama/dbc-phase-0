def get_grade(input)
	if input<=100 && input>=90
		return "A"
	elsif input<90 && input>=80
		return "B"
	elsif input<80 && input>=70
		return "C"
	elsif input<70 && input>=60
		return "D"
	elsif input<60
		return "F"
	end
end
		
		
		
